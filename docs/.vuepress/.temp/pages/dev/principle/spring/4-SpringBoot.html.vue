<p>SpringBoot 可以加快 Spring 项目的开发，主要特性包括自动装配、内嵌 Tomcat 服务器等。</p>
<p>首先，自动装配可以将第三方 jar 包中的 Bean 自动加载到容器中，无需额外配置。</p>
<p>第二，SpringBoot 内嵌 Tomcat 服务器，可以直接打包成 jar 包运行，无需打包成 war 包使用额外的 Tomcat 服务器。</p>
<h2 id="springboot-支持哪些嵌入-web-容器" tabindex="-1"><a class="header-anchor" href="#springboot-支持哪些嵌入-web-容器"><span>SpringBoot 支持哪些嵌入 Web 容器？</span></a></h2>
<p>SpringBoot 支持的嵌入 Web 容器有：Tomcat、Jetty、Undertow 和 Netty。</p>
<p>其中，Tomcat 是默认的嵌入 Web 容器，Jetty 和 Undertow 比较轻量，Netty 是高性能的网络通信框架。</p>
